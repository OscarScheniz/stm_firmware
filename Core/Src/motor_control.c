/**
  * @brief TIM17 Initialization Function
  * @param None
  * @retval None
  */
static void pwm_tim17_init(void)
{
  TIM_OC_InitTypeDef sConfigOC = {0};
  TIM_BreakDeadTimeConfigTypeDef sBreakDeadTimeConfig = {0};

  htim17.Instance = TIM17;
  htim17.Init.Prescaler = 71;
  htim17.Init.CounterMode = TIM_COUNTERMODE_UP;
  htim17.Init.Period = 19999;
  htim17.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
  htim17.Init.RepetitionCounter = 0;
  htim17.Init.AutoReloadPreload = TIM_AUTORELOAD_PRELOAD_DISABLE;
  if (HAL_TIM_Base_Init(&htim17) != HAL_OK)
  {
    Error_Handler();
  }
  if (HAL_TIM_PWM_Init(&htim17) != HAL_OK)
  {
    Error_Handler();
  }
  sConfigOC.OCMode = TIM_OCMODE_PWM1;
  sConfigOC.Pulse = PWM_PULSE_MIN;
  sConfigOC.OCPolarity = TIM_OCPOLARITY_HIGH;
  sConfigOC.OCNPolarity = TIM_OCNPOLARITY_HIGH;
  sConfigOC.OCFastMode = TIM_OCFAST_DISABLE;
  sConfigOC.OCIdleState = TIM_OCIDLESTATE_RESET;
  sConfigOC.OCNIdleState = TIM_OCNIDLESTATE_RESET;
  if (HAL_TIM_PWM_ConfigChannel(&htim17, &sConfigOC, TIM_CHANNEL_1) != HAL_OK)
  {
    Error_Handler();
  }
  sBreakDeadTimeConfig.OffStateRunMode = TIM_OSSR_DISABLE;
  sBreakDeadTimeConfig.OffStateIDLEMode = TIM_OSSI_DISABLE;
  sBreakDeadTimeConfig.LockLevel = TIM_LOCKLEVEL_OFF;
  sBreakDeadTimeConfig.DeadTime = 0;
  sBreakDeadTimeConfig.BreakState = TIM_BREAK_DISABLE;
  sBreakDeadTimeConfig.BreakPolarity = TIM_BREAKPOLARITY_HIGH;
  sBreakDeadTimeConfig.BreakFilter = 0;
  sBreakDeadTimeConfig.AutomaticOutput = TIM_AUTOMATICOUTPUT_DISABLE;
  if (HAL_TIMEx_ConfigBreakDeadTime(&htim17, &sBreakDeadTimeConfig) != HAL_OK)
  {
    Error_Handler();
  }

  timers_MspPostInit(&htim17);

}

void motor_control_set(MOTOR motor, uint8_t percentage)
{
  if(percentage > 100)
  {
      percentage = 100;
  }
  
  uint32_t pwm_value = PWM_PULSE_MIN + (percentage * (PWM_PULSE_MAX - PWM_PULSE_MIN)) / 100;

  switch(motor)
  {
      case Motor_0:
      __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pwm_value);
      break;

      case Motor_1:
      __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, pwm_value);
      break;

      case Motor_2:
      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, pwm_value);
      break;

      case Motor_3:
      __HAL_TIM_SET_COMPARE(&htim17, TIM_CHANNEL_1, pwm_value);
      break;
  }
}

void motor_control_start(void)
{
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim17, TIM_CHANNEL_1);
}

void motor_control_init(void)
{
  pwm_tim2_init();
  pwm_tim3_init();
  pwm_tim17_init();
}
